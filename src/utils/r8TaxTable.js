// 查表：amountAfterSocial = 支給合計 − 社保4项。返回该月源泉所得税（甲欄 0人）
export function lookupR8Tax0(amountAfterSocial) {
  const amt = Math.floor(amountAfterSocial || 0)
  if (amt < 105000) return 0
  // 二分查找最大 from ≤ amt
  let lo = 0, hi = R8_TABLE_0.length - 1
  while (lo < hi) {
    const mid = (lo + hi + 1) >> 1
    if (R8_TABLE_0[mid][0] <= amt) lo = mid
    else hi = mid - 1
  }
  return R8_TABLE_0[lo][1]
}
